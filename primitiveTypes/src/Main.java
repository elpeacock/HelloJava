public class Main {

    public static void main(String[] args) {
        System.out.println("Primitive Data Types in Java");
        Types.printByte();
        Types.printShort();
        Types.printInteger();
        Types.printDouble();
        Types.printLong();
        Types.printFloat();
        Types.printCharacter();
        Types.printBool();
    }

    /**
     * Created by user on 12/14/16.
     */
    public static class Types {

        public static void printByte() {
            Byte aByte = 122;
            System.out.println("I am a byte: " + aByte);
        }

        public static void printShort() {
            Short aShort = -12345;
            System.out.println("I am a short: " + aShort);
        }

        public static void printInteger() {
            Integer anInt = 1234567;
            System.out.println("I am an integer: " + anInt);
        }

        public static void printLong() {
            Long aLong = 123345678l;
            System.out.println("I am a long: " + aLong);
        }

        public static void printFloat() {
            Float aFloat = 3.1415f;
            System.out.println("I am a float: " + aFloat);
        }

        public static void printDouble() {
            Double aDouble = 1.23456789;
            System.out.println("I am a double: " + aDouble);
        }

        public static void printCharacter() {
            Character aCharacter = 'b';
            System.out.println("I am a character:" + aCharacter);
        }

        public static void printBool() {
            Boolean aBool = true;
            System.out.println("I am a boolean: " + aBool);
        }
    }
}
